package squareRoot;

//Public class named SquareRoot
public class SquareRoot 
{
	// A static method that calculates and prints the square root of the number 'n'
	public static void method(int n)
	{	
		System.out.print("Square Root Of a Number " + n + " is : "); // Display the expression for square root calculation
		System.out.print(Math.sqrt(n)); // Calculate and print the square root of 'n'
	}
}
